def student_test(questions):
    correct_answer = 0
    total_questios = len(questions)

    for i,question in enumerate(questions):
        print(i+1,". ",question["question"])
        user_input = input("Enter Your Answer: ").strip().upper()
        if user_input == question["correct_answer"]:
            correct_answer+=1
        
        
    score=(correct_answer/total_questios)*100
    print(score)
    return score



def run_tests():
    tests = [test1_questions, test2_questions, test3_questions]
    
    for i, test in enumerate(tests):
        print(f"\nStarting Test {i+1}")
        score = student_test(test)
        print(f"Your score: {score:.2f}%")
        
        if score < 70:
            print(f"You failed Test {i+1} with a score of {score:.2f}%. You need to pass with at least 70% to move on to the next test.")
            break
        else:
            print(f"You passed Test {i+1} with a score of {score:.2f}%.")
            if i == len(tests) - 1:
                print("Congratulations! You have passed all tests.")


run_tests()
